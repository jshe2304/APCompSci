package Chapter6;

import java.util.*;

public class powersOfTwo {
    public static void powersOfTwo() {
        for (int i = 1; i <= 10; i++) {
            System.out.println(Math.pow(2, i));
        }
    }
    
    public static void main(String[] args) {
        powersOfTwo();
    }
}

